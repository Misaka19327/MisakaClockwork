	public function setAuthenticatedUser($username, $id = null, $data = [])
	{
		$this->authenticatedUser = [
			'id'       => $id,
			'username' => $username,
			'email'    => $data['email'] ?? null,
			'name'     => $data['name'] ?? null
		];
